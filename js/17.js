/** Condicionales */

const disponible = 4000;
const retirar = 20000;

if (disponible > retirar) {
    /** Se cumple la condicion y se ejecuta este codigo */
    console.log('Si puedes retirar');
} else {
    /** No se cumple la condision, ejecutar este codigo */
    console.log('Lo siento, no puede retirar');
}

